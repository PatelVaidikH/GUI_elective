## If condition with only one case
a = 4
if a>5:
    print(a,' is greater than 5')

## If condition with two cases
a = 4
if a>5:
    print(a,' is greater than 5')
else:
    print(a,' is not greater than 5')

## If condition with multiple cases
a = 5
if a>5:
    print(a,' is greater than 5')
elif a<5:
    print(a,' is less than 5')
else:
    print(a,' is equal to 5')

